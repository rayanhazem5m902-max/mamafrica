function updateContent(lang) {
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });

    // Update impact translations for donation buttons
    document.querySelectorAll('[data-i18n-impact]').forEach(element => {
        const key = element.getAttribute('data-i18n-impact');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('data-impact', translations[lang][key]);
        }
    });

    // If an amount button is active, update the impact display immediately
    const activeBtn = document.querySelector('.amount-btn.active');
    if (activeBtn) {
        const impactDisplay = document.getElementById('impactDisplay');
        if (impactDisplay) {
            const span = impactDisplay.querySelector('span');
            const strong = impactDisplay.querySelector('strong');
            if (span) span.innerHTML = translations[lang]['donate.impact'] || 'Your donation will provide:';
            if (strong) strong.innerHTML = activeBtn.getAttribute('data-impact');
        }
    }

    localStorage.setItem('language', lang);

    // Update all toggle buttons (header, footer, mobile)
    const updateBtn = (btn) => {
        if (btn) {
            const targetText = lang === 'ar' ? 'English' : 'العربية';
            const span = btn.querySelector('span');
            if (span) {
                span.textContent = targetText;
            } else {
                btn.innerHTML = `<i class="fa-solid fa-earth-americas text-xl"></i> <span>${targetText}</span>`;
            }
        }
    };

    // Find by ID (legacy) and by class (new)
    const toggles = [
        document.getElementById('langToggle'),
        document.getElementById('langToggleMobile'),
        document.getElementById('langToggleFooter'),
        ...document.querySelectorAll('.lang-toggle')
    ];

    toggles.forEach(updateBtn);
}

function toggleLanguage() {
    const currentLang = document.documentElement.lang || 'en';
    const newLang = currentLang === 'en' ? 'ar' : 'en';
    updateContent(newLang);
}

// Make functions global
window.toggleLanguage = toggleLanguage;
window.updateContent = updateContent;

document.addEventListener('DOMContentLoaded', () => {
    // Initialize based on saved preference or default to Arabic
    const savedLang = localStorage.getItem('language') || 'ar';
    updateContent(savedLang);
});
